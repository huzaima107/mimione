public class Quest5 {

  public static int getTotal(int number1, int number2) {
    return number1 + number2;
  }

  public static void main(String[] args) {
    int number1 = 10;
    int number2 = 20;

    // Call the getTotal method and print the results.
    System.out.println("The total is " + getTotal(number1, number2));
  }
}
